package ex02;

public class LgTV {
	public void turnOn() {
		System.out.println("LgTV 전원 키기");
	}
	public void turnOff() {
		System.out.println("LgTV 전원 끄기");
	}
	public void soundUp() {
		System.out.println("LgTV 소리 키우기");
	}
	public void soundDown() {
		System.out.println("LgTV 소리 줄이기");
	}
	
}
